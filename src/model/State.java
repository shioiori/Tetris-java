package model;
public class State {
    protected int row0, row1, row2, row3, col0, col1, col2, col3;
    public State(int row0, int col0, int row1, int col1, int row2, int col2, int row3, int col3){
        this.row0 = row0;
        this.row1 = row1;
        this.row2 = row2;
        this.row3 = row3;
        this.col0 = col0;
        this.col1 = col1;
        this.col2 = col2;
        this.col3 = col3;
    }
}
